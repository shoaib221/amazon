export const Auth = () => {
    const [ login, toggle ] = useState(true);
    const [ option, changeOption ] = useState("Register");

    const onChange = () =>
    {
        if( option==="Register" ) 
        {
            changeOption("Login");
            toggle(false);
        }
        else
        {
            changeOption("Register");
            toggle(true);
        }
    }

    return (
        <div className="auth">

            { login && <Login/> }
            { !login && <Register /> }

            <button onClick={onChange} > { option } </button>

            <div> Or </div>

            <GoogleAuth />

        </div>
    )

}